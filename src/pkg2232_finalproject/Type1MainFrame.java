    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblMovies.getSelectedRow();
    if (selectedRow != -1) {
        int id = (int) tblMovies.getValueAt(selectedRow, 0); 
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this movie?", "Delete", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                java.sql.Connection conn = DBConnection.getConnection();
                String sql = "DELETE FROM Movie WHERE MovieID = ?";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setInt(1, id);
                pst.executeUpdate();
                conn.close();
                updateMovieTable(); 
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Delete error: " + e.getMessage());
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a movie to delete!");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

