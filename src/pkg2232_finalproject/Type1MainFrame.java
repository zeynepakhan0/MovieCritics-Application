    private void btnToggleRestrictionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnToggleRestrictionActionPerformed
       int selectedRow = tblMovies.getSelectedRow(); 
    
    if (selectedRow != -1) {
        int movieId = (int) tblMovies.getValueAt(selectedRow, 0); 
        
        try {
            java.sql.Connection conn = DBConnection.getConnection();
            
            String sql = "UPDATE Movie SET ParentalRestriction = NOT ParentalRestriction WHERE MovieID = ?";
            
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, movieId);
            
            int result = pst.executeUpdate();
            if (result > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Viewing rule updated successfully!");
                updateMovieTable(); 
            }
            conn.close();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage());
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a movie from the table first!");
    }
    }//GEN-LAST:event_btnToggleRestrictionActionPerformed

