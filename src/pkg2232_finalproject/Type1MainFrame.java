    public void updateMovieTableWithSearch(String key) {
       String[] columnNames = {"ID", "Title", "Year", "Genre", "Director", "Rating", "Restriction"};
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(columnNames, 0);
    
    try {
        java.sql.Connection conn = DBConnection.getConnection();
    
        String sql = "SELECT m.MovieID, m.Title, m.ReleaseYear, m.Genre, " +
                     "CONCAT(p.FirstName, ' ', p.LastName) AS DirectorName, m.Rating, m.ParentalRestriction " +
                     "FROM Movie m LEFT JOIN Person p ON m.DirectorId = p.PersonID " +
                     "WHERE (LOWER(m.Title) LIKE LOWER(?) " +
                     "OR LOWER(m.Genre) LIKE LOWER(?) " +
                     "OR CAST(m.ReleaseYear AS CHAR) LIKE ? " + 
                     "OR LOWER(p.FirstName) LIKE LOWER(?) " +
                     "OR LOWER(p.LastName) LIKE LOWER(?)) " +
                     "AND m.ParentalRestriction = FALSE"; 

        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        String searchPattern = "%" + key + "%";
        
        pst.setString(1, searchPattern);
        pst.setString(2, searchPattern);
        pst.setString(3, searchPattern);
        pst.setString(4, searchPattern);
        pst.setString(5, searchPattern);
        
        java.sql.ResultSet rs = pst.executeQuery();
        
        while(rs.next()) {
            Object[] row = {
                rs.getInt("MovieID"), 
                rs.getString("Title"), 
                rs.getInt("ReleaseYear"),
                rs.getString("Genre"), 
                rs.getString("DirectorName"),
                rs.getInt("Rating"), 
                rs.getBoolean("ParentalRestriction") ? "Restricted" : "Everyone"
            };
            model.addRow(row);
        }
        
        tblMovies.setModel(model);
        conn.close();
        
    } catch (Exception e) { 
        e.printStackTrace(); 
        javax.swing.JOptionPane.showMessageDialog(this, "Search Error: " + e.getMessage());
    }
}
