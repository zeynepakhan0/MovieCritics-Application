    private void btnAnalyticsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalyticsActionPerformed
        try {
        java.sql.Connection conn = DBConnection.getConnection();
        java.sql.Statement st = conn.createStatement();

        java.sql.ResultSet rsAvg = st.executeQuery("SELECT AVG(Rating) FROM Movie");
        double avg = 0;
        if (rsAvg.next()) {
            avg = rsAvg.getDouble(1);
        }

        
        java.sql.ResultSet rsMost = st.executeQuery("SELECT Title FROM Movie WHERE Watched = 1 ORDER BY Rating DESC LIMIT 1");
        String mostWatched = "No movies watched yet.";
        if (rsMost.next()) {
            mostWatched = rsMost.getString("Title");
        }

        javax.swing.JOptionPane.showMessageDialog(this, 
            "--- Family Movie Analytics ---\n" +
            "Average Rating: " + String.format("%.2f", avg) + "\n" +
            "Featured Watched Movie: " + mostWatched);

        conn.close();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Analytics Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAnalyticsActionPerformed

