public void setNextID() {
    try {
            java.sql.Connection conn = DBConnection.getConnection();
            String sql = "SELECT MAX(MovieID) FROM Movie";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                int maxId = rs.getInt(1);
                txtID.setText(String.valueOf(maxId + 1));
            } else {
                txtID.setText("1");
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
}
    private void loadMovieData() {
    try {
            java.sql.Connection conn = DBConnection.getConnection();

            String sql = "SELECT m.*, p.FirstName, p.LastName FROM Movie m "
                    + "LEFT JOIN Person p ON m.DirectorId = p.PersonID "
                    + "WHERE m.MovieID = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, mid);
            java.sql.ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txtID.setText(String.valueOf(rs.getInt("MovieID")));
                txtTitle.setText(rs.getString("Title"));
                txtYear.setText(String.valueOf(rs.getInt("ReleaseYear")));
                txtGenre.setText(rs.getString("Genre"));
                txtAbout.setText(rs.getString("About"));
                
                currentPosterPath = rs.getString("Poster");
                lblPosterPath.setText(currentPosterPath);

                String fName = rs.getString("FirstName") == null ? "" : rs.getString("FirstName");
                String lName = rs.getString("LastName") == null ? "" : rs.getString("LastName");
                txtDirectorName.setText((fName + " " + lName).trim());

                cmbRating.setSelectedItem(String.valueOf(rs.getInt("Rating")));
                chbRestricted.setSelected(rs.getBoolean("ParentalRestriction"));
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
}

