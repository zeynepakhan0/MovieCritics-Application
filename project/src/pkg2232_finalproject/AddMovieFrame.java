    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      String movieTitle = txtTitle.getText();
        String directorFullName = txtDirectorName.getText().trim();
        int rating = Integer.parseInt(cmbRating.getSelectedItem().toString());
        int directorId = -1;

        if (directorFullName.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a Director name!");
            return;
        }

        try {
            java.sql.Connection conn = DBConnection.getConnection();

            String[] nameParts = directorFullName.split(" ", 2);
            String fName = nameParts[0];
            String lName = (nameParts.length > 1) ? nameParts[1] : "";

            String checkPersonSql = "SELECT PersonID FROM Person WHERE UPPER(FirstName) = UPPER(?) AND UPPER(LastName) = UPPER(?)";
            java.sql.PreparedStatement pstCheck = conn.prepareStatement(checkPersonSql);
            pstCheck.setString(1, fName);
            pstCheck.setString(2, lName);
            java.sql.ResultSet rs = pstCheck.executeQuery();

            if (rs.next()) {
                directorId = rs.getInt("PersonID");
            } else {
                java.sql.Statement stMax = conn.createStatement();
                java.sql.ResultSet rsMax = stMax.executeQuery("SELECT MAX(PersonID) FROM Person");
                int maxPersonId = (rsMax.next()) ? rsMax.getInt(1) : 0;
                directorId = maxPersonId + 1;

                String insertPersonSql = "INSERT INTO Person (PersonID, FirstName, LastName) VALUES (?, ?, ?)";
                java.sql.PreparedStatement pstPerson = conn.prepareStatement(insertPersonSql);
                pstPerson.setInt(1, directorId);
                pstPerson.setString(2, fName);
                pstPerson.setString(3, lName);
                pstPerson.executeUpdate();
            }

       
            String sql;
            if (mid == -1) {
             
                sql = "INSERT INTO Movie (MovieID, Title, ReleaseYear, Genre, About, ParentalRestriction, DirectorId, Rating, Poster) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            } else {
             
                sql = "UPDATE Movie SET MovieID=?, Title=?, ReleaseYear=?, Genre=?, About=?, ParentalRestriction=?, DirectorId=?, Rating=?, Poster=? WHERE MovieID = ?";
            }

            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(txtID.getText()));
            pst.setString(2, movieTitle);
            pst.setInt(3, Integer.parseInt(txtYear.getText()));
            pst.setString(4, txtGenre.getText());
            pst.setString(5, txtAbout.getText());
            pst.setBoolean(6, chbRestricted.isSelected());
            pst.setInt(7, directorId);
            pst.setInt(8, rating);
            pst.setString(9, currentPosterPath); 

            if (mid != -1) {
                pst.setInt(10, mid);
            }

            pst.executeUpdate();

            String msg = (mid == -1) ? "Movie saved with poster!" : "Movie updated!";
            javax.swing.JOptionPane.showMessageDialog(this, msg);

            this.dispose();
            conn.close();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
                            
    
    }//GEN-LAST:event_btnSaveActionPerformed

